package com.sts.model;

public class MeterStruts {
	private String DRN;
	private String TI;
	private String KT;
	
	public MeterStruts(){
		
	}

	public String getDRN() {
		return DRN;
	}

	public void setDRN(String dRN) {
		DRN = dRN;
	}

	public String getTI() {
		return TI;
	}

	public void setTI(String tI) {
		TI = tI;
	}

	public String getKT() {
		return KT;
	}

	public void setKT(String kT) {
		KT = kT;
	}
	
}
